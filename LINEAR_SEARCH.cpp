#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main()
{
    int n,i,el,key;
    cin>>n;
    vector<int> vec(n);
    cout<<"Enter elements:";
    for(i=0;i<n;i++)
    {
        cin>>el;
        vec[i]=el;
        
    }
    cout<<"Enter key:";
    cin>>key;
    for(i=0;i<n;i++)
    {
        if(vec[i]==key)
        {
            cout<<"Key found at position : "<<i+1<<endl;
            break;
        }
    }
    cout<<"Not found";
}
